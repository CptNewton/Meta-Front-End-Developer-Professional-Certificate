// Write a "while" loop that will perform exactly the same repetitive code as this:
console.log(5);
console.log(4);
console.log(3);
console.log(2);
console.log(1);
console.log("Countdown finished!");

// Solution using while loop
var i = 5;
while (i > 0) {
  console.log(i);
  i--;
}

console.log("Countdown finished!");
