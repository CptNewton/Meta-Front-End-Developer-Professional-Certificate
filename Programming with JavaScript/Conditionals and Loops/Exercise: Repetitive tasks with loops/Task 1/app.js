// Write a "for" loop that will perform exactly the same repetitive code as this:
console.log(1);
console.log(2);
console.log(3);
console.log(4);
console.log(5);
console.log("Counting completed!");

// Solution using for loop
for (var i = 0; i <= 5; i++) {
  console.log(i);
}
console.log("Counting completed!");
