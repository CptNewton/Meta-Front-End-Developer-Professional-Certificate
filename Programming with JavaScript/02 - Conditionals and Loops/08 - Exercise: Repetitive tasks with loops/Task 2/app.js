// Write a "for" loop that will perform exactly the same repetitive code as this:
console.log(5);
console.log(4);
console.log(3);
console.log(2);
console.log(1);
console.log("Countdown finished!");

// Solution using for loop
for (var i = 5; i > 0; i--) {
  console.log(i);
}
console.log("Countdown finished!");
